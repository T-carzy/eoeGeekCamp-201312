package com.eoe.pre.oop.day01;

public class User {


			public int id;
			public String name;
			public String password;
			public String phone;
			public String email;
			public User() {
			
			}
			public User(int id, String name, String password, String phone,
					String email) {
				super();
				this.id = id;
				this.name = name;
				this.password = password;
				this.phone = phone;
				this.email = email;
			}
			

}

