package com.eoe.basic.day01;

public class Test04 {

	/**
	 * Java进行数学运算
	 */
	public static void main(String[] args) {
			 System.out.println("100+5="+(100+5));
			 System.out.println("100-5="+(100-5));
			 System.out.println("100*5="+(100*5));
			 System.out.println("100/5="+(100/5));
			 System.out.println("100%5="+(100%5));
			 System.out.println("10的五次方="+Math.pow(10,5));
			 System.out.println("-10的绝对值="+Math.abs(-10));
			 System.out.println("121开平方根="+Math.sqrt(121));
			 System.out.println("5/3="+Math.round(5.0/3));         //四舍五入
			 System.out.println("5/3="+Math.floor(5.0/3));           //取整
	}

}
