package com.eoe.basic.day02;

public class Test23 {
	/**
	 * 示例：自增、自减
	 */
	public static void main(String[] args) {
		int i=100;
		System.out.println("i++:"+i++);
		i=100;
		System.out.println("++i:"+(++i));
		i=100;
		System.out.println("i+=5:"+(i+=5));
		i=100;
		System.out.println("i--:"+i--);
		i=100;
		System.out.println("--i:"+--i);
		i=100;
		System.out.println("i-5="+(i-=5));
	}
}
